def PairSum(arr, x):
    kvl = {}
    # Dictionary Creation
    for i in arr:
        if i not in kvl:
            kvl.update({i: 1})
        else:
            prevVal = kvl.get(i)
            kvl.update({i: (prevVal + 1)})
    for j in arr:
        if (x - j) in kvl:
            cnt = kvl.get(x - j)
            if j < (x - j):
                for k in range(cnt):
                    print(j, (x - j))
            elif j == (x - j):
                for u in range(cnt - 1):
                    print(j, (x - j))
                kvl.update({i: (cnt - 1)})
    # print(kvl)


